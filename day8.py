def encrypt(text, shift):
    encrypted_text = ""
    for i in text:
        if i.isalpha():
           num= alphabet.index(i)
           num=(num+shift)%26
           encrypted_text+=alphabet[num] 
        else:
            encrypted_text+=" "   
        
    print(encrypted_text)      

def decrypt(text,shift):
    decrypted_text=""
    for i in text:
        if i.isalpha():
            num=alphabet.index(i)
            num=abs(num-shift)%26 
            decrypted_text+=alphabet[num]
        else:
            decrypted_text+=" "    
        
    print(decrypted_text)         



